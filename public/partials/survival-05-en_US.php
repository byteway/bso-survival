<h2>How to play</h2>
<p>Two teams start at the same time</p>

<h2>Team 1</h2>
<ol>
<li>From the start area to A</li>
<li>Move your team from A to B using the inner tube</li>
<li>Pull the truck over the indicated distance (indicated by red/white ribbon)</li>
<li>Participants one by one with the cable car from C to D</li>
<li>Walk to F and move your team from F to G using the inner tube</li>
<li>Participants one by one with the cable car from C to D</li>
<li>Back to the finish area, when everyone is inside the finish area the time stops.</li>
</ol>

<h2>Team 2</h2>
<ol>
<li>From the start box to F</li>
<li>Move your team from F to G using the inner tube</li>
<li>Participants one by one with the cable car from C to D</li>
<li>Walk to A and move your team from A to B using the inner tube</li>
<li>Pull the truck over the indicated distance (indicated by red/white ribbon)</li>
<li>Participants one by one with the cable car from C to D</li>
<li>Back to the finish area, when everyone is inside the finish area the time stops.</li>
</ol>

