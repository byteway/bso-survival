<h1>Kano sprint</h1>
<p><b>Location</b>: Trees at Komijnepad
<br><b>Number of participants</b>: 8 participants per team
<br><b>Playing time</b>: 20 minutes
<br><b>Penalties</b>: N/A</p>

<emp>PAY ATTENTION ! THIS GAME IS PLAYED PER TEAM ! TWO TEAMS CAN'T PLAY AT THE SAME TIME !</emp><br>

<h2>How to play</h2>
<ol>
<li>Move the two canoes out of the start/finish area A and onto the water.</li>
<li>Lower the canoes into the water and paddle as fast as possible but the end of the ditch (B)</li>
<li>One of the participants from the canoe rings the bell and then returns, through the water, to the start/finish section A. Then one of the participants also starts from the second canoe the bell.</li>
<li>Leave the water with the canoes and put them (with the paddles) back in the start/finish area</li>
<li>Time stops when everyone, including the paddled canoes, is in the finish area.</li>
</ol>

<h2>Penalty</h2>
<ul>
<li><b>points</b>: 20 penalty seconds for not ringing the bell (point B).</li>
