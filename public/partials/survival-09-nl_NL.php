<h1>Vlotten bouwen</h1>
<p><b>Plaats</b>: Waterpartij bij de tennisbanen
<br><b>Aantal deelnemers</b>: 8 deelnemers per team
<br><b>Speeltijd</b>: 20 minuten
<br><b>Sancties</b>: Zie hieronder</p>

<h2>Spelregels</h2>
<ol>
<li>Bouw een vlot met het team met de 4 binnenbanden.</li>
<li>Twee personen (met gele vesten) lopen naar A en varen met de mortiertub naar het wisselpunt.</li>
<li>Zeven personen varen met het vlot naar het wisselpunt.</li>
<li>Halverwege wordt er gewisseld (zie tekening).</li>
<li>Twee deelnemers van het vlot gaan in de mortiertub en de deelnemers van de mortiertub gaan op het vlot.</li>
<li>Vaar terug naar het startpunt (waar je het water in ging).</li>
<li>Alle items (inclusief mortiertub) moeten teruggeplaatst worden in het start- en finishgebied.</li>
<li>Demonteer de vlotten en plaats de mortiertub terug in het start-/finishgebied, de tijd stopt.</li>
</ol>

<h2>Sancties</h2>
<p>Boetetijd: 1 minuut voor het in het water gaan. 2 minuten voor het missen van het wisselpunt.</p>