<h1>Water dragen</h1>
<p><b>Location</b>: next to the bridge to Weidesteenlaan
<br><b>Number of participants</b>: 8 participants per team
<br><b>Playing time</b>: 20 minutes
<br><b>Penalties</b>: See below</p>

<h2>How to play</h2>
<p><b>Goal</b>: Bring as much water as possible from A to B.<p>
<ol>
<li>Six participants on the rope above the water</li>
<li>Fill the buckets with water and pass it around</li>
<li>The empty buckets have to be returned via the participants over the rope</li>
</ol>

<h2>Penalty time</h2>
<ul>
<li>30 seconds for each bucket that falls into the water</li>
<li>2 1/2 minutes for each participant that falls into the water.</li>
<ul>