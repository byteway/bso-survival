<h1>Survival Track</h1>
<p><b>Location</b>: Grass field in front of the tent/First Aid
<br><b>Number of participants</b>: 8 participants per team
<br><b>Playing time</b>: 20 minutes
<br><b>Penalties</b>: points</p>

<h2>How to play</h2>
<p>One participant from each team starts with a ball and tries to overcome the obstacle as quickly as possible. The next participant will start if they are tagged out by the previous participant. Only when all participants of a team have been there, the first participant starts to take the obstacle again.
<br>The assignment is to complete in 20 min. allow as many participants as possible to complete the obstacle.</p>