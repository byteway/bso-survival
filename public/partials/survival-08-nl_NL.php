<h1>Water dragen</h1>
<p><b>Plaats</b>: naast de brug naar Weidesteenlaan
<br><b>Aantal deelnemers</b>: 8 deelnemers per team
<br><b>Speeltijd</b>: 20 minuten
<br><b>Sancties</b>: Zie hieronder</p>

<h2>Spelregels</h2>
<p><b>Doel</b>: Zo veel mogelijk water van A naar B brengen.</p>
<ol>
<li>Zes deelnemers op het touw boven het water.</li>
<li>Vul de emmers met water en geef ze door.</li>
<li>De lege emmers moeten via de deelnemers over het touw worden teruggenomen.</li>
</ol>

<h2>Sancties</h2>
<ul>
<li>30 seconden voor elke emmer die in het water valt.</li>
<li>2 1/2 minuten voor elke deelnemer die in het water valt.</li>
</ul>
