<h1>Scooping water</h1>
<p><b>Location</b>: Near water in front of Valeriaanweg
<br><b>Number of participants</b>: 8 participants per team
<br><b>Playing time</b>: 20 minutes
<br><b>Penalties</b>: See below</p>

<h2>How to play</h2>
<p>Two teams start at the same time. 
<br>Climb above the water via the net and fill the bucket with water and climb back up via the net. 
<br>The bucket has to go over the bridge railing and then empty into the bucket.</p>

<h2>Penalty time</h2>
<ul>
<li>30 seconds for each bucket that falls into the water</li>
<li>2 1/2 minutes for each participant that falls into the water</li>
</ul>

<br><emp>MAXIMUM 2 PARTICIPANTS IN THE NET!</emp>