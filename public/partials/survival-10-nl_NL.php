<h1>Kano sprint</h1>
<p><b>Plaats</b>: Bij de bomen op het Komijnepad
<br><b>Aantal deelnemers</b>: 8 deelnemers per team
<br><b>Speeltijd</b>: 20 minuten
<br><b>Sancties</b>: n.v.t.</p>

<emp>LET OP! DIT SPEL WORDT PER TEAM GESPEELD! TWEE TEAMS KUNNEN NIET TEGELIJK SPELEN!</emp><br>

<h2>Spelregels</h2>
<ol>
<li>Verplaats de twee kano's uit het start-/finishgebied A naar het water.</li>
<li>Laat de kano's in het water zakken en peddel zo snel mogelijk naar het einde van de sloot (B).</li>
<li>Eén van de deelnemers van de kano luidt de bel en keert vervolgens via het water terug naar het start-/finishgedeelte A. Dan begint ook één van de deelnemers van de tweede kano met het luiden van de bel.</li>
<li>Verlaat het water met de kano's en plaats ze (met de peddels) terug in het start-/finishgebied.</li>
<li>De tijd stopt wanneer iedereen, inclusief de gepeddeld kano's, in het finishgebied is.</li>
</ol>

<h2>Sancties</h2>
<ul>
<li><b>punten</b>: 20 strafseconden voor het niet luiden van de bel (punt B).</li>
