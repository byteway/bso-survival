<h1>Vlotten bouwen</h1>
<p><b>Location</b>: Water feature for tennis courts
<br><b>Number of participants</b>: 8 participants per team
<br><b>Playing time</b>: 20 minutes
<br><b>Penalties</b>: See below</p>

<h2>How to play</h2>
<ol>
<li>Build a raft with the team with the 4 inner tubes.</li>
<li>Two people (wearing yellow vests) walk to A and sail with the mortar tub to the change point.</li>
<li>Seven people sail with the raft to the exchange point</li>
<li>Change the pond halfway through (see drawing).</li>
<li>Two participants of raft go into mortar tub and the participants of the mortar tub go on raft.</li>
<li>Sail back to the starting point (where you entered the water).</li>
<li>All items (including mortar tub) must be put back in the start and finish area.</li>
<li>Disassemble rafts and mortar tub back in start/finish area, time stops</li>
</ol>

<h2>Penalty</h2>
<p>Penalty time 1 min. for getting into the water.2 min. for missing the change point</p>