<h1>Waterscheppen</h1>
<p><b>Locatie</b>: Bij het water voor Valeriaanweg
<br><b>Aantal deelnemers</b>: 8 deelnemers per team
<br><b>Speeltijd</b>: 20 minuten
<br><b>Boetes</b>: Zie hieronder</p>

<h2>Spelregels</h2>
<p>Twee teams starten tegelijkertijd.
<br>Klim boven het water via het net en vul de emmer met water. Klim vervolgens terug omhoog via het net.
<br>De emmer moet over de brugleuning gaan en daarna in de andere emmer worden geleegd.</p>

<h2>Boetetijd</h2>
<ul>
<li>30 seconden voor elke emmer die in het water valt</li>
<li>2 1/2 minuten voor elke deelnemer die in het water valt</li>
</ul>

<br><emp>MAXIMAAL 2 DEELNEMERS IN HET NET!</emp>
